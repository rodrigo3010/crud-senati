    private void Agregar_usuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Agregar_usuarioActionPerformed
        // TODO add your handling code here:
        Formulario_Usuarios f_u=new Formulario_Usuarios();
        Conexion cn=new Conexion();
        int id=Integer.parseInt(Id.getText());
        String nombre=Nombre.getText();
        String apellido=Apellido.getText();
        String query="INSERT INTO Usuario(id,Nombre,Apellido)VALUES(?,?,?)";
        try{
            PreparedStatement ps= cn.conectar().prepareStatement(query);
            ps.setInt(1, id);
            ps.setString(2,nombre);
            ps.setString(3, apellido);
            int rowsaffect=ps.executeUpdate();
            if(rowsaffect > 0){
                System.out.println("Se cargaron los datos");
            }else{
                System.out.println("No se cargaron los datos");
            }
        }catch(SQLException e){
            System.out.println(e);
        }
        Object ag[]=new Object[3];
        ag[0]=id;
        ag[1]=nombre;
        ag[2]=apellido;
        f_u.tabla.addRow(ag);

    }//GEN-LAST:event_Agregar_usuarioActionPerformed
    
    public void  cargarUsuarios(){
        System.out.println("cargar usuario");
        Conexion cn =new Conexion();//Instancia
        cn.conectar();
        
        String query="select * from usuario";
        
        try{
            Statement st;
            st = cn.conectar().createStatement();
            ResultSet rs = st.executeQuery(query); 
            if (rs != null){
                System.out.println("SE CARGARON LOS DATOS");
            }
            String[] headers = {"ID","Name","Lastname"};
            DefaultTableModel model = new DefaultTableModel(headers,0);
           // model.addColumn(headers);
            
            while(rs.next()){
                
                
                
                Object[] row ={
                    rs.getInt("id"),
                    rs.getString("name"),
                    rs.getString("lastname")  
            };  
            model.addRow(row);
            }
            //tblUsuarios.setModel(model);
        }catch (SQLException e){
            System.out.println(e);
        }
        
          
        
        
    }
    
